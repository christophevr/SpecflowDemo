namespace SpecflowDemo.Calculator
{
    public class Calculator
    {
        public int Som(int eersteGetal, int tweedeGetal)
        {
            return eersteGetal + tweedeGetal;
        }

        public int Product(int eersteGetal, int tweedeGetal)
        {
            return eersteGetal*tweedeGetal;
        }
    }
}
